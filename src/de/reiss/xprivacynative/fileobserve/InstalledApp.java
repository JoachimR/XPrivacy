package de.reiss.xprivacynative.fileobserve;


public class InstalledApp {

    public String userId;
    public String packageName;

    @Override
    public String toString() {
        return "InstalledApp{" +
                "userId='" + userId + '\'' +
                ", packageName='" + packageName + '\'' +
                '}' + "\n\n\n";
    }
}
